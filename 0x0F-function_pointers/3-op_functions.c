#include "calc.h"

/**
 * op_add = add two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub = sub two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul = mul two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div = divide two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod = add two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
